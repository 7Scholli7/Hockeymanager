<h2>Regeln</h2>

<h3>1. Das Spielprinzip</h3>

<p>Jeder Verein bzw. Mitspieler bekommt pro Saison eine bestimmte Anzahl von Toren. Diese werden unterteilt in Heimtore und Auswärtstore. Diese Tore müssen nun Spieltag für Spieltag geschickt auf die Spieltage verteilt werden.
Beispiel: Die Adler Mannheim spielen gegen die Kölner Haie. Der Manager der Adler setzt 3 Tore seines Heimkontos, der Gast aus Köln setzt 1 Tor seines Auswärtskontos. Das Spiel endet 3:1 für die Adler. Die gesetzten Tore werden nach dem Spiel von den jeweiligen Torkonten abgezogen: Mannheim 3 Tore vom Heimkonto, Köln 1 Tor vom Auswärtskonto.</p>

<h3>2. Die Torkonten</h3>
<p>In der ersten Saison erhalten alle Vereine die gleiche Anzahl an Heim- und Auswärtstoren. Ab Saison 2 gibt es je nach Platzierung ein anderes Torkonto.
In neu eingeführten Ligen erhalten alle Vereine die gleiche Anzahl von Toren.
Es ist keine Übernahme von Resttoren (nach Saisonende) in die darauffolgende Saison möglich.</p>

<p>Der Sieger der jeweiligen nationalen Meisterschaft erhält zur kommenden Saison +2 Tore (Heim und auswärts) für sein Ligakonto.</p>

<p>Der Sieger der jeweiligen nationalen Pokale erhält zur kommenden Saison +1 Tor (Heim und auswärts) für sein Ligakonto.</p>

<p>Der Eishockey-to-Dream-Manager des Jahres erhält zur kommenden Saison +1 Tor (auswärts) für sein Ligakonto.</p>

<h3>3. Bonustore</h3>

<h3>4. Vereinstreue</h3>

<p>Jeder Manager der vom 1. Bis zum letzten Spieltag seinem Verein treu geblieben ist, erhält 2 Bonustore zur freien Verteilung.</p>

<h3>5. Trainingslager</h3>
<p>Vor dem Beginn einer Saison (ab Saison 2) werden in allen Ligen Trainingslager angeboten. Ort und Beschreibung der Trainingslager geben Hinweise zu deren „Auswirkungen“. Man kann durch geschicktes Auswählen des jeweiligen Lagers Tore gewinnen. Verpflichtend sind die Trainingslager aber für keinen Manager.</p>

<h3>6. Champions Hockey League</h3>
<p>Findet ab Saison 2 statt.</p>

<h3>7. Das Setzen</h3>
<p>Unsinniges Setzen:
Es liegt im Ermessen des Ligaleiters offensichtlich unsinnige Einsätze (z.B. extrem Hohe Anzahl von Toren) für ungültig zu erklären.</p>

<h3>8. Trainerentlassungen, Strafen, Nichtsetzen</h3>
<p>Sportliche Entlassung:
Ein Manager wird entlassen, wenn er nach der Auswertung eines ZATs in den letzten 10 Spielen weniger als 4 Punkte geholt hat.

Entlassung wegen Nichtsetzens:
Ein Manager wird sofort wieder entlassen, wenn er beim ersten Spiel seiner Amtszeit nicht setzt.
Ein Manager wird entlassen, wenn er zweimal in Folge im gleichen Wettbewerb nicht setzt.
Nichtsetzen gilt nicht Saisonübergreifend!

Entlassung wegen Doppelmanagertätigkeit:
Es ist nicht erlaubt, sich zweimal anzumelden. Wer das doch tut, wird sofort wieder entlassen.

Entlassung wegen Ergebnisabsprache:
Es ist nicht erlaubt Ergebnisabsprachen zu treffen! Geschieht das trotzdem und lässt es sich nachweisen, werden alle beteiligten Managers nach Beratung und Beschluss des Vorstands ggf. entlassen!</p>

